    private void jButtonCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastroActionPerformed
        ClienteModel objDao = new ClienteModel();              
        Cliente objDto = new Cliente();
                
        objDto.setNome(jTextFieldNome.getText());
        objDto.setCpf(jTextFieldCPF.getText());
        objDto.setEmail(jTextFieldEmail.getText());
        objDto.setTelefone(jTextFieldTelefone.getText());
        objDto.setEndereco(jTextFieldEndereco.getText());        
        objDto.setCidade(jTextFieldCidade.getText());
        
        objDao.inserir(objDto);
        
        JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
    }//GEN-LAST:event_jButtonCadastroActionPerformed
    
